    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
                                                 
    String nombre = campoNombre.getText();
    String apellido = campoApellido.getText();
    String telefono = campoTel.getText();
    String usuario = jTextField1.getText();
    String password = new String(jPasswordField1.getPassword()); // Convertir password a texto
    
    if (nombre.isEmpty() || apellido.isEmpty() || telefono.isEmpty() || usuario.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "⚠️ Todos los campos son obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    boolean registroExitoso = Conexion.registrarUsuario(nombre, apellido, telefono, usuario, password);
    
    if (registroExitoso) {
        JOptionPane.showMessageDialog(this, "✅ Usuario registrado correctamente.");
        this.dispose(); // Cierra la ventana de registro
    } else {
        JOptionPane.showMessageDialog(this, "❌ Error al registrar usuario.", "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_jButton2ActionPerformed

