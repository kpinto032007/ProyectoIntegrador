    private void guardarDatos() {
    String nombre = campoNombre.getText();
    String apellido = campoApellido.getText();
    String telefono = campoTel.getText();
    String usuario = jTextField1.getText();
    String contraseña = new String(jPasswordField1.getPassword()); // Mejor manera de obtener la contraseña

    // Verificar que los campos no estén vacíos
    if (nombre.isEmpty() || apellido.isEmpty() || telefono.isEmpty() || usuario.isEmpty() || contraseña.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios", "Advertencia", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Establecer conexión con la base de datos
    Connection con = Conexion.conectar();
    if (con == null) {
        JOptionPane.showMessageDialog(this, "Error al conectar con la base de datos", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Consulta SQL corregida (agregando estado con valor 1 por defecto)
    String sql = "INSERT INTO tb_usuarios (nombre, apellido, telefono, usuario, password, estado) VALUES (?, ?, ?, ?, ?, ?)";

    try {
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, nombre);
        pst.setString(2, apellido);
        pst.setString(3, telefono);
        pst.setString(4, usuario);
        pst.setString(5, contraseña);
         pst.setInt(6, 1);  // Estado fijo en 1
        
        pst.executeUpdate();
        
        // Mensaje de éxito
        JOptionPane.showMessageDialog(this, "Registro exitoso");

        // Limpiar campos
        campoNombre.setText("");
        campoApellido.setText("");
        campoTel.setText("");
        jTextField1.setText("");
        jPasswordField1.setText("");

        pst.close(); // Cerrar el PreparedStatement
        con.close(); // Cerrar conexión

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al guardar: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

}




